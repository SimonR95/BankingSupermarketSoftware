    public double power(double input1, double input2) {
        double powerresult = 1;
        for (int i = 1; i <= input2; i++) {
            powerresult *= input1;
        }
        return powerresult;
    }

    public double factorial(double input1) {
        double factorialresult = 1;
        for (int i = 1; i <= input1; i++) {
            factorialresult *= i;
        }
        return factorialresult;
    }

    public double sin(double input1) {

        double sinresult = 0;
        for (int i = 0; i < 80; i++) {
            sinresult += power(-1, i) * ((power(input1, (2 * i) + 1)) / factorial((2 * i) + 1));
        }
        return sinresult;
    }

    public double cos(double input1) {

        double cosresult = 0;
        for (int i = 0; i < 80; i++) {
            cosresult += power(-1, i) * ((power(input1, (2 * i))) / factorial((2 * i)));
        }
        return cosresult;
    }

    public double tan(double input1) {
        double tanresult = sin(input1) / cos(input1);
        return tanresult;
    }

    public double SinPlusCos(double input1) {
        double sinpluscosresult = power(sin(input1), 2) + power(cos(input1), 2);
        return sinpluscosresult;
    }

    public double SinCosTan(double input1) {
        double SinCosTanResult = sin(input1) - cos(input1) + tan(input1);
        return SinCosTanResult;
    }

    public void Squareroot(double input3) {

        double guess1;
        if (input3 == 0) {
            jCalculatorInputField.setText("0");
        } else {
            CalculatorTotal = input3 / 2;
            do {
                guess1 = CalculatorTotal;
                CalculatorTotal = (guess1 + (input3 / guess1)) / 2;
            } while ((guess1 - CalculatorTotal) != 0);

            jCalculatorInputField.setText(String.valueOf(CalculatorTotal));
        }
    }

