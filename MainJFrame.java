    public void bankingSoftwareTransaction(String strFirstNameIdentifier, String strSurnameIdentifier, int intAccountNumber, double dblTotalPrice) {
        FoundCustomer = BankClients.find(CustomerInfo, strFirstNameIdentifier, strSurnameIdentifier);
        BankClients.ClearCustomerAccounts();
        BankClients.ReadCustomerAccounts();
        Date date = new Date();
        new SimpleDateFormat("dd/MM/yyyy").format(date);
        for (int i = 0; i < FoundCustomer.accounts.size(); i++) {
            if (intAccountNumber == FoundCustomer.accounts.get(i).accountNo) {
                FoundCustomer.accounts.get(i).withdraw(dblTotalPrice, date);
                FoundCustomer.updateLastRecordedDate(intAccountNumber, date);
            }
        }
        FoundCustomer = BankClients.find(CustomerInfo, "FSB", "Supermarket");
        FoundCustomer.accounts.get(0).deposit(dblTotalPrice);
        FoundCustomer.updateLastRecordedDate(1, date);
        CustomerInfo.EmptyAccountsFile();
        BankClients.PrintCustomerAccounts();
    }

